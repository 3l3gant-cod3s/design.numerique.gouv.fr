        <li>Définir une stratégie globale pour l’évolution des services en ligne rendus aux usagers, tenant compte des décisions gouvernementales, des besoins des usagers, et des possibilités de mise en œuvre par les administrations. A ce titre, il mesure et veille à la qualité des principaux services en ligne (notamment de leur design et de l’expérience utilisateur), développe la mise en œuvre du principe « dites-le-nous une fois » et la prise en compte de l’accessibilité par les personnes en situation de handicap</li>
        <li>Faire évoluer et piloter la mise en œuvre la politique de mesure des usages des services en ligne et de la satisfaction des usagers sur ces services en ligne (en particulier la mise à jour d’un tableau de bord des services publics numériques - observatoire.numerique.gouv.fr)</li>
        <li>Fournir ressources et aide aux administrations pour la mise en œuvre et l’améliorations de leurs services en ligne : normes, référentiels, guides, outils, briques  réutilisables (librairie de composants, API, etc.) dispositifs d’accompagnement</li>
        <li>Animer un réseau de correspondants dans les administrations</li>
        <li>Participer au développement d’une culture de Design au sein de l’État, et à la mise en communauté de compétences de design dans les administrations</li>

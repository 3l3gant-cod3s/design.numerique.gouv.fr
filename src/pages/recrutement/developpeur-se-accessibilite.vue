      <h2>Présentation du service</h2>
      <p>La DINUM est un service du Premier ministre, placé sous l’autorité administrative du Secrétaire Général du Gouvernement, et relevant du ministre de l’Action et des Comptes publics, du ministre de l’Economie et des Finances et du secrétaire d’Etat chargé du Numérique.</p>
      <p>Elle définit la stratégie relative à la transformation numérique de l’Etat et coordonne sa mise en œuvre.</p>
      <p>Elle promeut l’innovation numérique comme levier de transformation du service public et des méthodes et outils de travail des agents publics.</p>
      <p>Elle veille à ce que les systèmes d’information et le développement du numérique concourent à l’amélioration de la qualité, l’efficacité, l’efficience et la fiabilité du service rendu et à la simplification des relations entre les usagers et les administrations de l’Etat, et entre celles-ci et les autres autorités administratives.</p>
      <p>Elle opère des services d’infrastructures à valeur ajoutée mutualisés entre les administrations, dont le réseau interministériel de l'Etat (RIE).</p>
      <p>Dans le cadre de ces objectifs, <strong>le pôle Design des services numériques est chargé de</strong> :</p>
      <ul>
        <li>Définir une stratégie globale pour l’évolution des services en ligne rendus aux usagers, tenant compte des décisions gouvernementales, des besoins des usagers, et des possibilités de mise en œuvre par les administrations. A ce titre, il mesure et veille à la qualité des principaux services en ligne (notamment de leur design et de l’expérience utilisateur), développe la mise en œuvre du principe « dites-le-nous une fois » et la prise en compte de l’accessibilité par les personnes en situation de handicap</li>
        <li>Faire évoluer et piloter la mise en œuvre la politique de mesure des usages des services en ligne et de la satisfaction des usagers sur ces services en ligne (en particulier la mise à jour d’un tableau de bord des services publics numériques - observatoire.numerique.gouv.fr)</li>
        <li>Fournir ressources et aide aux administrations pour la mise en œuvre et l’améliorations de leurs services en ligne : normes, référentiels, guides, outils, briques  réutilisables (librairie de composants, API, etc.) dispositifs d’accompagnement</li>
        <li>Animer un réseau de correspondants dans les administrations</li>
        <li>Participer au développement d’une culture de Design au sein de l’État, et à la mise en communauté de compétences de design dans les administrations</li>
        <li>Stimuler, en lien avec l’Agence Nationale de la Cohésion des Territoire (ANCT), en interministériel le développement d’actions en faveur d’une meilleure inclusion de l’ensemble des usagers dans le cadre de la transformation numérique des services publics</li>
      </ul>

      <h2>Missions</h2>
      <p>Accompagner les administrations et leurs équipes techniques dans l’accélération de la mise en accessibilité de leurs services pour les personnes en situation de handicap, et plus largement sur l’amélioration de la qualité de leurs services :</p>
      <ul>
        <li>Développer des outils mieux adaptés aux équipes techniques pour faciliter l’internalisation, par les différents acteurs dans les administrations (chefs-ffes de projets/produits, designers, développeurs-ses) des enjeux de l’accessibilité des services numériques aux personnes en situation de handicap</li>
        <li>Accompagner les porteurs des services numériques sur la mise en accessibilité de leurs démarches administratives et les orienter vers des dispositifs d’accompagnement complémentaires en fonction de leurs besoins (audits, recommandations, de l’aide au diagnostic, intervention de designers, etc.) et atteindre l’objectif fixé par le gouvernement : 80% des démarches de l’Observatoire (observatoire.numerique.gouv.fr) satisfaisant au critère « accessibilité » en  2022. Plus généralement, accompagner ces acteurs pour améliorer la qualité et la satisfaction des usagers de leurs services numériques.</li>
        <li>Produire et/ou diffuser des ressources, organiser des événements et animer le réseau des porteurs de démarches administratives</li>
        <li>Participer aux évolutions du RGAA, expertise technique sur le RGAA</li>
      </ul>
      <h2>Activités</h2>
      <ul>
        <li>Développer, intégrer et/ou référencer de nouveaux outils pour faciliter la prise en main de l’accessibilité et maintenir des outils existants</li>
        <li>Audits, conseils et formation : réaliser des audits d’accessibilité pour faire avancer les équipes et les accompagner dans l’internalisation de ces compétences</li>
        <li>Événements : contribution à l’organisation d’événements liés à l’accessibilité numérique ainsi qu’à l’amélioration globale de la qualité des services, animation, participation, interventions</li>
        <li>Ressources : contribuer à la production de ressources et outils plus pertinents à une prise en main rapide du sujet par les développeurs, designers et cadres</li>
      </ul>


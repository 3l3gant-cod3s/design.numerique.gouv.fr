      </ul>

      <h2>Missions</h2>
      <p>À titre principal, porter la stratégie interministérielle d’accessibilité aux personnes en situation de handicap des services administratifs en ligne, et à titre complémentaire participer à l’animation des travaux des ministères dans l’amélioration de la qualité de leurs démarches ne ligne. Dans ce cadre, le titulaire du poste devra :</p>
      <ul>
        <li>Accompagner les administrations sur l’accélération de la mise en accessibilité de leurs services numériques, et plus globalement sur l’amélioration de la qualité de leurs services</li>
        <li>Développer des stratégies assurant la prise en compte de l’accessibilité numérique dans les services numériques des administrations, ainsi que la formation des équipes</li>
        <li>Piloter la déclinaison des engagements pris en matière d’accessibilité par le gouvernement et dans la réglementation</li>
        <li>Orienter les porteurs des services numériques vers les dispositifs d’appui complémentaires adaptés aux besoins spécifiques de chacune de leurs démarches en ligne (audit accessibilité, aide au diagnostic, intervention de designers, etc.)</li>
        <li>Assurer la relation avec les partenaires (FIPHFP, SIG, etc.) et prestataires de la DINUM dans le cadre de l’accessibilité numérique et piloter l’évolution des relations contractuelles avec ces acteurs</li>
        <li>S’assurer que les administrations porteuses de la démarche en ligne élaborent des plans d’actions d’amélioration de la qualité et de l’accessibilité de leurs démarches en ligne, si besoin l’assister dans cette élaboration, puis veiller à sa mise en œuvre</li>
        <li>Assurer la souveraineté et le leadership de la France dans le cadre de la stratégie Européenne sur l’accessibilité numérique et des évolutions du RGAA</li>
      </ul>
